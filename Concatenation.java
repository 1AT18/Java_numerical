public class Concatenation {
    public static void main(String[] args) {
        String s1 ="6";
        int s2 = 12;
        System.out.println(s1+s2);
        System.out.println("hello"+"java");
        System.out.println(10+"10");
        System.out.println(10+10);
        System.out.println("Akash"+"gowda");
    }
}
